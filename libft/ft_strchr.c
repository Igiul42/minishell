#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;
	char	val;
	int		i;

	i = ft_strlen(s);
	str = (char *)s;
	val = (char)c;
	while (i >= 0)
	{
		if (*str == val)
			return (str);
		str++;
		i--;
	}
	return (NULL);
}
