#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	l_s;

	l_s = ft_strlen(src);
	if (l_s + 1 < size)
		ft_memcpy(dest, src, l_s + 1);
	else if (size != 0)
	{
		ft_memcpy(dest, src, size - 1);
		dest[size - 1] = '\0';
	}
	return (l_s);
}
